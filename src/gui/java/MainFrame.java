    private void orderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderBtnActionPerformed
        
        //Stores the book ISBN entered in textbox to bookISBN variable
        String bookISBN = bookISBNbox.getText();
        String bookCopies = copierOrderInput.getText();
        
        //Checks for EMpty field
        if(bookISBN.length() == 0)
            JOptionPane.showMessageDialog(null, "Empty fields detected ! Please fill up book ISBN");
        else if(bookCopies.length() == 0)
            JOptionPane.showMessageDialog(null, "Empty fields detected ! Please fill up book copies");
        else{
            
        
        /*
        
        
        
        
            Query to order the input where isbn = bookISBN from input box and copies = bookCopies from copies input box
        
        
        
        
        */
        String result = "Book "+bookISBN+" is ordered"+bookCopies+" copies.\n";
        JOptionPane.showMessageDialog(null, result);
        //bookISBNResult.setText(bookISBN);
        bookISBNbox.setText("");
        copierOrderInput.setText("");
        }
    }//GEN-LAST:event_orderBtnActionPerformed

    
   
