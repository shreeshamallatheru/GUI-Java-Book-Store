        //ISBN Search button action
            
        //Reads the input from search box
        String inputIsbn = searchBox.getText();
        
        
        //Checks for empty input
        if(inputIsbn.length() == 0)
            JOptionPane.showMessageDialog(null, "Empty fields detected ! Please fill up search box");
        else{
            

            /*





            Query to fetch the table content where isbn = inputIsbn from text box;




            */
            //Displays the result's to outputbox
            searchBox.setText("");
            searchResult.setText(inputIsbn);
        }
